.text
.global _start
_start:
            ldr     r0, =0x53000000     @ WATCHDOG的地址·
            mov     r1, #0x0
            str   r1, [r0]              @ 关闭看门狗

            ldr     sp, =1024*4         @ 设置sp栈指针,4k
                                        @
            bl      main                @ 跳转到main函数
halt_loop:
            b       halt_loop
